#!/usr/bin/env node
// Deterministic contracts generator.
// Reads contracts/schema/*.schema.json and emits TypeScript and Rust modules
// under contracts/generated/. Output is stable: no timestamps, no machine
// paths, fixed ordering. Files are rewritten only when content changes.

import { mkdirSync, readdirSync, readFileSync, writeFileSync, existsSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const schemaDir = join(repoRoot, "contracts", "schema");
const tsOutDir = join(repoRoot, "contracts", "generated", "ts");
const rustOutDir = join(repoRoot, "contracts", "generated", "rust");

const HEADER = `// This file is generated by tools/gen-contracts.mjs.
// Do not edit by hand.
`;

const failures = [];
const fail = (msg) => failures.push(msg);

// ---------------------------------------------------------------------------
// Naming helpers
// ---------------------------------------------------------------------------

const PASCAL_RE = /^[A-Z][A-Za-z0-9]*$/;
const ALL_CAPS_RE = /^[A-Z][A-Z0-9_]*$/;
const CAMEL_RE = /^[a-z][A-Za-z0-9]*$/;

const toPascal = (s) => s.charAt(0).toUpperCase() + s.slice(1);
const toSnake = (s) => s.replace(/([A-Z])/g, "_$1").toLowerCase().replace(/^_/, "");
const toScreamingSnake = (s) => toSnake(s).toUpperCase();

const RUST_KEYWORDS = new Set([
  "as", "async", "await", "become", "box", "break", "const", "continue", "crate", "do",
  "dyn", "else", "enum", "extern", "false", "final", "fn", "for", "if", "impl", "in",
  "let", "loop", "macro", "match", "mod", "move", "mut", "override", "priv", "pub",
  "ref", "return", "self", "static", "struct", "super", "trait", "true", "try", "type",
  "typeof", "unsafe", "unsized", "use", "virtual", "where", "while", "yield",
]);
const rustIdent = (s) => (RUST_KEYWORDS.has(s) ? `r#${s}` : s);

// ---------------------------------------------------------------------------
// Load and validate schemas
// ---------------------------------------------------------------------------

const PRIMITIVES = new Set(["string", "boolean", "number", "integer", "json"]);

function validateType(type, where, refs) {
  if (typeof type === "string") {
    if (!PRIMITIVES.has(type)) fail(`${where}: unsupported primitive type "${type}"`);
    return;
  }
  if (typeof type !== "object" || type === null || Array.isArray(type)) {
    fail(`${where}: invalid type form`);
    return;
  }
  const keys = Object.keys(type);
  if (keys.length !== 1) {
    fail(`${where}: type object must have exactly one of array/map/ref`);
    return;
  }
  const [key] = keys;
  if (key === "array" || key === "map") {
    validateType(type[key], `${where}.${key}`, refs);
  } else if (key === "ref") {
    if (typeof type.ref !== "string") fail(`${where}: ref must be a string`);
    else refs.push({ ref: type.ref, where });
  } else {
    fail(`${where}: unsupported type key "${key}"`);
  }
}

function checkKeys(obj, allowed, where) {
  for (const key of Object.keys(obj)) {
    if (!allowed.includes(key)) fail(`${where}: unsupported key "${key}"`);
  }
}

function validateFields(fields, where, refs) {
  if (!Array.isArray(fields) || fields.length === 0) {
    fail(`${where}: must declare at least one field`);
    return;
  }
  for (const field of fields) {
    checkKeys(field, ["name", "type", "doc", "optional"], `${where}.${field.name ?? "?"}`);
    if (typeof field.name !== "string" || !CAMEL_RE.test(field.name)) {
      fail(`${where}: field name "${field.name}" must be camelCase`);
    }
    if (field.optional !== undefined && typeof field.optional !== "boolean") {
      fail(`${where}.${field.name}: optional must be boolean`);
    }
    validateType(field.type, `${where}.${field.name}`, refs);
  }
}

const schemaFiles = readdirSync(schemaDir).filter((f) => f.endsWith(".schema.json")).sort();
if (schemaFiles.length === 0) fail("no schema files found under contracts/schema/");

const domains = [];
for (const file of schemaFiles) {
  let parsed;
  try {
    parsed = JSON.parse(readFileSync(join(schemaDir, file), "utf8"));
  } catch (e) {
    fail(`${file}: invalid JSON (${e.message})`);
    continue;
  }
  checkKeys(parsed, ["domain", "declarations"], file);
  if (typeof parsed.domain !== "string" || !/^[a-z]+$/.test(parsed.domain)) {
    fail(`${file}: domain must be a lowercase string`);
    continue;
  }
  if (!Array.isArray(parsed.declarations)) {
    fail(`${file}: declarations must be an array`);
    continue;
  }
  if (file !== `${parsed.domain}.schema.json`) {
    fail(`${file}: file name must match domain "${parsed.domain}"`);
  }
  domains.push(parsed);
}
domains.sort((a, b) => (a.domain < b.domain ? -1 : 1));

// Global declaration registry (variant names are reserved too).
const registry = new Map(); // name -> { kind, domain, decl }
const pendingRefs = [];

for (const schema of domains) {
  for (const decl of schema.declarations) {
    const where = `${schema.domain}.${decl.name ?? "?"}`;
    if (typeof decl.name !== "string") {
      fail(`${schema.domain}: declaration without a name`);
      continue;
    }
    const expectedName = decl.kind === "constant" ? ALL_CAPS_RE : PASCAL_RE;
    if (!expectedName.test(decl.name)) {
      fail(`${where}: name must be ${decl.kind === "constant" ? "ALL_CAPS" : "PascalCase"}`);
    }
    if (typeof decl.doc !== "string" || decl.doc.length === 0) {
      fail(`${where}: doc is required`);
    }
    const register = (name, kind, extra = {}) => {
      if (registry.has(name)) fail(`duplicate declaration name across schemas: ${name}`);
      registry.set(name, { kind, domain: schema.domain, decl, ...extra });
    };
    switch (decl.kind) {
      case "enum":
        checkKeys(decl, ["kind", "name", "doc", "values"], where);
        if (!Array.isArray(decl.values) || decl.values.length === 0) {
          fail(`${where}: enum must have at least one value`);
        } else {
          for (const v of decl.values) {
            if (typeof v !== "string" || !CAMEL_RE.test(v)) {
              fail(`${where}: enum value "${v}" must be a camelCase string`);
            }
          }
        }
        register(decl.name, "enum");
        break;
      case "struct":
        checkKeys(decl, ["kind", "name", "doc", "fields"], where);
        validateFields(decl.fields, where, pendingRefs);
        register(decl.name, "struct");
        break;
      case "union": {
        checkKeys(decl, ["kind", "name", "doc", "tag", "variants"], where);
        if (typeof decl.tag !== "string" || !CAMEL_RE.test(decl.tag)) {
          fail(`${where}: union tag must be a camelCase string`);
        }
        if (!Array.isArray(decl.variants) || decl.variants.length < 2) {
          fail(`${where}: union must have at least two variants`);
          break;
        }
        register(decl.name, "union");
        const seenTags = new Set();
        for (const variant of decl.variants) {
          const vWhere = `${where}.${variant.name ?? "?"}`;
          checkKeys(variant, ["name", "tagValue", "doc", "fields"], vWhere);
          if (typeof variant.name !== "string" || !PASCAL_RE.test(variant.name)) {
            fail(`${vWhere}: variant name must be PascalCase`);
          }
          if (typeof variant.tagValue !== "string" || !CAMEL_RE.test(variant.tagValue)) {
            fail(`${vWhere}: tagValue must be a camelCase string`);
          }
          if (seenTags.has(variant.tagValue)) fail(`${vWhere}: duplicate tagValue`);
          seenTags.add(variant.tagValue);
          validateFields(variant.fields, vWhere, pendingRefs);
          register(variant.name, "variant", { union: decl.name });
        }
        break;
      }
      case "alias":
        checkKeys(decl, ["kind", "name", "doc", "of"], where);
        validateType(decl.of, where, pendingRefs);
        register(decl.name, "alias");
        break;
      case "constant":
        checkKeys(decl, ["kind", "name", "doc", "type", "value"], where);
        if (decl.type === undefined || decl.value === undefined) {
          fail(`${where}: constant must have type and value`);
        } else {
          validateType(decl.type, where, pendingRefs);
        }
        register(decl.name, "constant");
        break;
      default:
        fail(`${where}: unsupported declaration kind "${decl.kind}"`);
    }
  }
}

for (const { ref, where } of pendingRefs) {
  const target = registry.get(ref);
  if (!target) fail(`${where}: ref "${ref}" does not resolve to a declared type`);
  else if (target.kind === "variant") fail(`${where}: ref "${ref}" points to a union variant`);
  else if (target.kind === "constant") fail(`${where}: ref "${ref}" points to a constant`);
}

if (failures.length > 0) {
  for (const msg of failures) console.error(`FAIL: ${msg}`);
  console.error("gen-contracts: FAILED");
  process.exit(1);
}

// ---------------------------------------------------------------------------
// Shared emission helpers
// ---------------------------------------------------------------------------

function collectRefs(type, out) {
  if (typeof type === "string") {
    if (type === "json") out.add("JsonValue");
    return;
  }
  if (type.array) collectRefs(type.array, out);
  else if (type.map) collectRefs(type.map, out);
  else if (type.ref) out.add(type.ref);
}

function domainExternalRefs(schema) {
  const used = new Set();
  for (const decl of schema.declarations) {
    if (decl.kind === "struct") for (const f of decl.fields) collectRefs(f.type, used);
    if (decl.kind === "alias") collectRefs(decl.of, used);
    if (decl.kind === "constant") collectRefs(decl.type, used);
    if (decl.kind === "union") {
      for (const v of decl.variants) for (const f of v.fields) collectRefs(f.type, used);
    }
  }
  const byDomain = new Map(); // domain -> sorted names
  for (const name of used) {
    const target = registry.get(name);
    if (!target || target.domain === schema.domain) continue;
    if (!byDomain.has(target.domain)) byDomain.set(target.domain, new Set());
    byDomain.get(target.domain).add(name);
  }
  return new Map(
    [...byDomain.entries()]
      .sort(([a], [b]) => (a < b ? -1 : 1))
      .map(([d, names]) => [d, [...names].sort()]),
  );
}

const sortKeys = (value) => {
  if (Array.isArray(value)) return value.map(sortKeys);
  if (value && typeof value === "object") {
    return Object.fromEntries(
      Object.keys(value)
        .sort()
        .map((k) => [k, sortKeys(value[k])]),
    );
  }
  return value;
};

// ---------------------------------------------------------------------------
// TypeScript emission
// ---------------------------------------------------------------------------

function tsType(type) {
  if (typeof type === "string") {
    switch (type) {
      case "string": return "string";
      case "boolean": return "boolean";
      case "number": return "number";
      case "integer": return "number";
      case "json": return "JsonValue";
    }
  }
  if (type.array) return `${tsType(type.array)}[]`;
  if (type.map) return `Record<string, ${tsType(type.map)}>`;
  return type.ref;
}

function tsFields(fields, indent) {
  const lines = [];
  for (const field of fields) {
    if (field.doc) lines.push(`${indent}/** ${field.doc} */`);
    lines.push(`${indent}${field.name}${field.optional ? "?" : ""}: ${tsType(field.type)};`);
  }
  return lines;
}

function emitTsDomain(schema) {
  const lines = [HEADER];
  const imports = domainExternalRefs(schema);
  for (const [domain, names] of imports) {
    lines.push(`import type { ${names.join(", ")} } from "./${domain}.js";`);
  }
  if (imports.size > 0) lines.push("");
  for (const decl of schema.declarations) {
    switch (decl.kind) {
      case "alias":
        lines.push(`/** ${decl.doc} */`);
        if (decl.name === "JsonValue") {
          lines.push(
            "export type JsonValue = null | boolean | number | string | JsonValue[] | { [key: string]: JsonValue };",
          );
        } else {
          lines.push(`export type ${decl.name} = ${tsType(decl.of)};`);
        }
        lines.push("");
        break;
      case "enum": {
        const valuesName = `${toScreamingSnake(decl.name)}_VALUES`;
        lines.push(`/** ${decl.doc} */`);
        lines.push(`export const ${valuesName} = [${decl.values.map((v) => `"${v}"`).join(", ")}] as const;`);
        lines.push(`export type ${decl.name} = (typeof ${valuesName})[number];`);
        lines.push("");
        break;
      }
      case "struct":
        lines.push(`/** ${decl.doc} */`);
        lines.push(`export interface ${decl.name} {`);
        lines.push(...tsFields(decl.fields, "  "));
        lines.push("}");
        lines.push("");
        break;
      case "union":
        lines.push(`/** ${decl.doc} */`);
        lines.push(`export type ${decl.name} = ${decl.variants.map((v) => v.name).join(" | ")};`);
        lines.push("");
        for (const variant of decl.variants) {
          lines.push(`export interface ${variant.name} {`);
          lines.push(`  ${decl.tag}: "${variant.tagValue}";`);
          lines.push(...tsFields(variant.fields, "  "));
          lines.push("}");
          lines.push("");
        }
        break;
      case "constant": {
        const json = JSON.stringify(sortKeys(decl.value), null, 2);
        lines.push(`/** ${decl.doc} */`);
        lines.push(`export const ${decl.name} = ${json} as const;`);
        lines.push("");
        break;
      }
    }
  }
  return `${lines.join("\n").trimEnd()}\n`;
}

// ---------------------------------------------------------------------------
// Rust emission
// ---------------------------------------------------------------------------

function rustType(type) {
  if (typeof type === "string") {
    switch (type) {
      case "string": return "String";
      case "boolean": return "bool";
      case "number": return "f64";
      case "integer": return "i64";
      case "json": return "JsonValue";
    }
  }
  if (type.array) return `Vec<${rustType(type.array)}>`;
  if (type.map) return `BTreeMap<String, ${rustType(type.map)}>`;
  return type.ref;
}

function rustFields(fields, indent, pub) {
  const lines = [];
  for (const field of fields) {
    if (field.doc) lines.push(`${indent}/// ${field.doc}`);
    const name = rustIdent(toSnake(field.name));
    if (field.optional) {
      lines.push(`${indent}#[serde(skip_serializing_if = "Option::is_none")]`);
      lines.push(`${indent}${pub ? "pub " : ""}${name}: Option<${rustType(field.type)}>,`);
    } else {
      lines.push(`${indent}${pub ? "pub " : ""}${name}: ${rustType(field.type)},`);
    }
  }
  return lines;
}

function rustValue(type, value) {
  if (typeof type === "string") {
    switch (type) {
      case "string": return `${JSON.stringify(value)}.to_string()`;
      case "boolean": return value ? "true" : "false";
      case "integer": return `${value}`;
      case "number": return Number.isInteger(value) ? `${value}.0` : `${value}`;
      case "json": return `serde_json::json!(${JSON.stringify(sortKeys(value))})`;
    }
  }
  if (type.array) {
    return `vec![${value.map((v) => rustValue(type.array, v)).join(", ")}]`;
  }
  if (type.map) {
    const inserts = Object.keys(value)
      .sort()
      .map((k) => `map.insert(${JSON.stringify(k)}.to_string(), ${rustValue(type.map, value[k])});`);
    return `{\n        let mut map = BTreeMap::new();\n        ${inserts.join("\n        ")}\n        map\n    }`;
  }
  const target = registry.get(type.ref);
  if (target?.kind === "enum" && typeof value === "string") {
    return `${type.ref}::${toPascal(value)}`;
  }
  if (target?.kind === "alias") {
    return rustValue(target.decl.of, value);
  }
  throw new Error(`unsupported constant value for ref ${type.ref}`);
}

function domainUsesJson(schema) {
  const used = new Set();
  for (const decl of schema.declarations) {
    if (decl.kind === "struct") for (const f of decl.fields) collectRefs(f.type, used);
    if (decl.kind === "alias") collectRefs(decl.of, used);
    if (decl.kind === "constant") collectRefs(decl.type, used);
    if (decl.kind === "union") for (const v of decl.variants) for (const f of v.fields) collectRefs(f.type, used);
  }
  return used.has("JsonValue");
}

function domainUsesMap(schema) {
  const walk = (type) => {
    if (typeof type === "string") return false;
    if (type.map) return true;
    if (type.array) return walk(type.array);
    return false;
  };
  for (const decl of schema.declarations) {
    if (decl.kind === "struct" && decl.fields.some((f) => walk(f.type))) return true;
    if (decl.kind === "alias" && walk(decl.of)) return true;
    if (decl.kind === "constant" && walk(decl.type)) return true;
    if (decl.kind === "union" && decl.variants.some((v) => v.fields.some((f) => walk(f.type)))) return true;
  }
  return false;
}

function emitRustDomain(schema) {
  const lines = [HEADER];
  const hasSerdeItems = schema.declarations.some((d) => ["enum", "struct", "union"].includes(d.kind));
  if (hasSerdeItems) lines.push("use serde::{Deserialize, Serialize};");
  if (domainUsesMap(schema)) lines.push("use std::collections::BTreeMap;");
  const imports = domainExternalRefs(schema);
  for (const [domain, names] of imports) {
    lines.push(`use super::${domain}::{${names.join(", ")}};`);
  }
  lines.push("");
  for (const decl of schema.declarations) {
    switch (decl.kind) {
      case "alias":
        lines.push(`/// ${decl.doc}`);
        if (decl.name === "JsonValue") {
          lines.push("pub type JsonValue = serde_json::Value;");
        } else {
          lines.push(`pub type ${decl.name} = ${rustType(decl.of)};`);
        }
        lines.push("");
        break;
      case "enum":
        lines.push(`/// ${decl.doc}`);
        lines.push("#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]");
        lines.push('#[serde(rename_all = "camelCase")]');
        lines.push(`pub enum ${decl.name} {`);
        for (const value of decl.values) {
          lines.push(`    ${toPascal(value)},`);
        }
        lines.push("}");
        lines.push("");
        break;
      case "struct":
        lines.push(`/// ${decl.doc}`);
        lines.push("#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]");
        lines.push('#[serde(rename_all = "camelCase")]');
        lines.push(`pub struct ${decl.name} {`);
        lines.push(...rustFields(decl.fields, "    ", true));
        lines.push("}");
        lines.push("");
        break;
      case "union":
        lines.push(`/// ${decl.doc}`);
        lines.push("#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]");
        lines.push(`#[serde(tag = "${decl.tag}")]`);
        lines.push(`pub enum ${decl.name} {`);
        for (const variant of decl.variants) {
          lines.push(`    #[serde(rename = "${variant.tagValue}", rename_all = "camelCase")]`);
          lines.push(`    ${toPascal(variant.tagValue)} {`);
          lines.push(...rustFields(variant.fields, "        ", false));
          lines.push("    },");
        }
        lines.push("}");
        lines.push("");
        break;
      case "constant": {
        lines.push(`/// ${decl.doc}`);
        lines.push(`pub fn ${toSnake(decl.name.toLowerCase()) || decl.name.toLowerCase()}() -> ${rustType(decl.type)} {`);
        lines.push(`    ${rustValue(decl.type, decl.value)}`);
        lines.push("}");
        lines.push("");
        break;
      }
    }
  }
  return `${lines.join("\n").trimEnd()}\n`;
}

// ---------------------------------------------------------------------------
// Write outputs (only when changed)
// ---------------------------------------------------------------------------

let wrote = 0;
function writeIfChanged(path, content) {
  mkdirSync(dirname(path), { recursive: true });
  if (!existsSync(path) || readFileSync(path, "utf8") !== content) {
    writeFileSync(path, content);
    wrote += 1;
  }
}

for (const schema of domains) {
  writeIfChanged(join(tsOutDir, `${schema.domain}.ts`), emitTsDomain(schema));
  writeIfChanged(join(rustOutDir, `${schema.domain}.rs`), emitRustDomain(schema));
}

const tsBarrel = `${HEADER}${domains.map((s) => `export * from "./${s.domain}.js";`).join("\n")}\n`;
writeIfChanged(join(tsOutDir, "index.ts"), tsBarrel);

const rustBarrel = `${HEADER}${domains.map((s) => `pub mod ${s.domain};`).join("\n")}\n`;
writeIfChanged(join(rustOutDir, "mod.rs"), rustBarrel);

console.log("gen-contracts: OK");
